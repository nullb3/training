// Literal float
Console.WriteLine(3.14159F); // Lowercase f works too.

// Literal decimal
Console.WriteLine(3.14159M); // Lowercase m works too.

// Float Type    Precision
// ----------------------------
// float         ~6-9 digits
// double        ~15-17 digits
// decimal        28-29 digits

// Boolean
Console.WriteLine(true);
